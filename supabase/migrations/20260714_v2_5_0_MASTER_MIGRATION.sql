drop policy if exists "Authenticated users view open marketplace loans" on public.marketplace_loans;
create policy "Authenticated users view open marketplace loans"
on public.marketplace_loans for select to authenticated
using (status in ('Open','Funded') or public.is_secured_landing_admin());

drop policy if exists "Admins manage marketplace loans" on public.marketplace_loans;
create policy "Admins manage marketplace loans"
on public.marketplace_loans for all to authenticated
using (public.is_secured_landing_admin())
with check (public.is_secured_landing_admin());

drop policy if exists "Borrowers view own generated documents" on public.generated_loan_documents;
drop policy if exists "Borrowers can view their generated documents" on public.generated_loan_documents;
create policy "Borrowers view own generated documents"
on public.generated_loan_documents for select to authenticated
using (borrower_user_id = auth.uid() or public.is_secured_landing_admin());

drop policy if exists "Borrowers acknowledge own generated documents" on public.generated_loan_documents;
create policy "Borrowers acknowledge own generated documents"
on public.generated_loan_documents for update to authenticated
using (borrower_user_id = auth.uid() or public.is_secured_landing_admin())
with check (borrower_user_id = auth.uid() or public.is_secured_landing_admin());

drop policy if exists "Admins manage generated documents" on public.generated_loan_documents;
drop policy if exists "Admins can manage generated loan documents" on public.generated_loan_documents;
create policy "Admins manage generated documents"
on public.generated_loan_documents for all to authenticated
using (public.is_secured_landing_admin())
with check (public.is_secured_landing_admin());

