create extension if not exists pgcrypto;

-- Shared admin helper
create or replace function public.is_secured_landing_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.admin_users where user_id = auth.uid()
  );
$$;

