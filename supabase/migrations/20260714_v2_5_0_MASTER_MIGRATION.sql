-- Generated loan documents. Compatibility columns are retained so old and new code can coexist.
create table if not exists public.generated_loan_documents (
  id uuid primary key default gen_random_uuid(),
  loan_application_id bigint not null references public.loan_applications(id) on delete cascade,
  borrower_user_id uuid references auth.users(id) on delete cascade,
  borrower_id uuid references auth.users(id) on delete cascade,
  document_type text not null,
  document_name text,
  title text,
  storage_path text,
  status text not null default 'ready_for_review',
  terms_snapshot jsonb not null default '{}'::jsonb,
  generated_at timestamptz not null default now(),
  sent_at timestamptz,
  acknowledged_at timestamptz,
  signed_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.generated_loan_documents
  add column if not exists borrower_user_id uuid references auth.users(id),
  add column if not exists borrower_id uuid references auth.users(id),
  add column if not exists document_name text,
  add column if not exists title text,
  add column if not exists storage_path text,
  add column if not exists terms_snapshot jsonb default '{}'::jsonb,
  add column if not exists generated_at timestamptz not null default now(),
  add column if not exists sent_at timestamptz,
  add column if not exists acknowledged_at timestamptz,
  add column if not exists signed_at timestamptz,
  add column if not exists updated_at timestamptz not null default now();

update public.generated_loan_documents
set borrower_user_id = coalesce(borrower_user_id, borrower_id)
where borrower_user_id is null;

update public.generated_loan_documents
set title = coalesce(title, document_name, initcap(replace(document_type, '_', ' '))),
    document_name = coalesce(document_name, title, initcap(replace(document_type, '_', ' ')))
where title is null or document_name is null;

create unique index if not exists generated_loan_documents_loan_type_uidx
  on public.generated_loan_documents(loan_application_id, document_type);
create index if not exists generated_loan_documents_borrower_user_idx
  on public.generated_loan_documents(borrower_user_id, loan_application_id);

-- Closing center
create table if not exists public.loan_closings (
  id uuid primary key default gen_random_uuid(),
